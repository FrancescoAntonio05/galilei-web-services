import fs from "node:fs";
import openapiTS from "openapi-typescript";

const OPENAPI_FILE = new URL("./../../../api/openapi.json", import.meta.url);
const OUTPUT_FILE = new URL("../src/openapi.ts", import.meta.url);

const contents = await openapiTS(OPENAPI_FILE);

fs.writeFileSync(
  OUTPUT_FILE,
  ["// -- file generated by openapi-typescript don't edit --", contents].join(
    "\n\n"
  )
);
